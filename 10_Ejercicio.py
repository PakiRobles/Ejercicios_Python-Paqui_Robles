#Ejercicio 10: Determinar el Día de la Semana
#Escribe un programa que determine el día de la semana correspondiente a un
#número ingresado por el usuario ((1 para lunes, 2 para martes, etc.).

dia = int(input("Indica el número del día de la semana del 1 al 7 : "))
if dia == 1 :
  print("El día de la semana es lunes")
if dia == 2 :
  print("El día de la semana es martes")
if dia == 3 :
  print("El día de la semana es miercoles")
if dia == 4 :
  print("El día de la semana es jueves")
if dia == 5 :
  print("El día de la semana es viernes")
if dia == 6 :
  print("El día de la semana es sábado")
if dia == 7 :
  print("El día de la semana es domingo")
  