#Ejercicio 13: Verificación de Número Primo
#Escribe un programa que determine si un número ingresado por el usuario es primoo no.
numero = int(input("Ingresa un numero para determinar si es primo : "))
if numero % 2 == 0 :
  print("el numero ", numero, "es primo")
else :
  print ("el número", numero, "no es primo")


